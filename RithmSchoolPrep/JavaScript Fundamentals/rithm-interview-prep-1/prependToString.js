function prependToString(str1, str2) {
  return str2 + str1;
}

console.log(prependToString("awesome", "very")); // 'veryawesome'
console.log(prependToString("world", "hello ")); // 'hello world'
console.log(prependToString("nothing", "")); // 'nothing'
